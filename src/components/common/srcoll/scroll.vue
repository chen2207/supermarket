<template>
  <div class="wrapper" ref="wrapper">
    <div class="content">
      <slot></slot>
    </div>
  </div>
</template>

<script>
import BScroll from 'better-scroll'
export default {
  data(){
    return {
      scroll:null
    }
  },
  props:{
    probeType:{
      type:Number,
      default:0
    },
    pullUpLoad:{
      type:Boolean,
      default:true
    }
  },
  mounted(){
      this.scroll=new BScroll(this.$refs.wrapper,{
        observeDOM: true,
        observeImage:true,
        mouseWheel:true,
        click:true,
        probeType:this.probeType,
        pullUpLoad:this.pullUpLoad,
    })
    this.scroll.on('scroll',(position)=>{
      this.$emit('scroll',position)
    })
    this.scroll.on('pullingUp',()=>{
      this.$emit('pullingUp')
    })
  }
}
</script>

